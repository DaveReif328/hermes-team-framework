#!/bin/bash
#
# Hermes Team Framework - Setup Script
# Initializes the Hermes environment for deployment
#

set -euo pipefail

# =============================================================================
# CONSTANTS
# =============================================================================

readonly SCRIPT_NAME="$(basename "${BASH_SOURCE[0]}")"
readonly SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
readonly HERMES_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
readonly CONFIG_DIR="${HERMES_ROOT}/config"
readonly LOG_DIR="${HERMES_ROOT}/logs"
readonly TMP_DIR="${HERMES_ROOT}/tmp"

# Version info
readonly HERMES_VERSION="1.0.0"
readonly SETUP_VERSION="1.0.0"

# Colors for output
readonly COLOR_RESET='\033[0m'
readonly COLOR_RED='\033[0;31m'
readonly COLOR_GREEN='\033[0;32m'
readonly COLOR_YELLOW='\033[0;33m'
readonly COLOR_BLUE='\033[0;34m'

# =============================================================================
# LOGGING FUNCTIONS
# =============================================================================

log_info() {
    echo -e "${COLOR_BLUE}[INFO]${COLOR_RESET} $*"
}

log_success() {
    echo -e "${COLOR_GREEN}[SUCCESS]${COLOR_RESET} $*"
}

log_warning() {
    echo -e "${COLOR_YELLOW}[WARNING]${COLOR_RESET} $*"
}

log_error() {
    echo -e "${COLOR_RED}[ERROR]${COLOR_RESET} $*"
}

log_debug() {
    if [[ "${DEBUG:-0}" == "1" ]]; then
        echo -e "[DEBUG] $*"
    fi
}

# =============================================================================
# PRE-FLIGHT CHECKS
# =============================================================================

check_prerequisites() {
    log_info "Running prerequisites check..."

    local missing_deps=0

    # Check for required commands
    local required_cmds=("bash" "git" "curl" "jq")
    for cmd in "${required_cmds[@]}"; do
        if ! command -v "$cmd" &> /dev/null; then
            log_error "Required command not found: $cmd"
            missing_deps=1
        fi
    done

    # Check bash version (4.0+)
    local bash_version="${BASH_VERSION%%.*}"
    if [[ "${bash_version}" -lt 4 ]]; then
        log_error "Bash 4.0+ required, found: ${BASH_VERSION}"
        missing_deps=1
    fi

    # Check for optional but recommended commands
    local recommended_cmds=("python3" "docker" "make")
    for cmd in "${recommended_cmds[@]}"; do
        if ! command -v "$cmd" &> /dev/null; then
            log_warning "Recommended command not found: $cmd"
        fi
    done

    if [[ "${missing_deps}" -eq 1 ]]; then
        log_error "Prerequisites check failed. Please install missing dependencies."
        exit 1
    fi

    log_success "Prerequisites check passed"
}

check_filesystem() {
    log_info "Checking filesystem..."

    # Check if HERMES_ROOT exists and is writable
    if [[ ! -d "${HERMES_ROOT}" ]]; then
        log_error "Hermes root directory not found: ${HERMES_ROOT}"
        exit 1
    fi

    if [[ ! -w "${HERMES_ROOT}" ]]; then
        log_error "Hermes root directory not writable: ${HERMES_ROOT}"
        exit 1
    fi

    log_success "Filesystem check passed"
}

# =============================================================================
# ENVIRONMENT SETUP
# =============================================================================

create_directories() {
    log_info "Creating directory structure..."

    local dirs=(
        "${CONFIG_DIR}"
        "${LOG_DIR}"
        "${TMP_DIR}"
        "${HERMES_ROOT}/data"
        "${HERMES_ROOT}/data/knowledge"
        "${HERMES_ROOT}/data/tasks"
        "${HERMES_ROOT}/data/archives"
        "${HERMES_ROOT}/scripts"
        "${HERMES_ROOT}/reports"
    )

    for dir in "${dirs[@]}"; do
        if [[ ! -d "${dir}" ]]; then
            mkdir -p "${dir}"
            log_debug "Created directory: ${dir}"
        else
            log_debug "Directory exists: ${dir}"
        fi
    done

    log_success "Directory structure created"
}

