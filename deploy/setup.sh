# =============================================================================
# UTILITY SCRIPTS
# =============================================================================

create_utility_scripts() {
    log_info "Creating utility scripts..."

    local scripts_dir="${HERMES_ROOT}/scripts"
    
    # Agent status script
    cat > "${scripts_dir}/agent-status.sh" << 'EOF'
#!/bin/bash
# Agent status check utility

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
HERMES_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"

# Source environment if available
if [[ -f "${HERMES_ROOT}/config/.env" ]]; then
    source "${HERMES_ROOT}/config/.env"
fi

echo "Checking agent status..."
echo "========================"

# Read agent registry
AGENTS_YAML="${HERMES_ROOT}/config/agents.yaml"

if command -v yq &> /dev/null; then
    AGENT_COUNT=$(yq '.agents | length' "${AGENTS_YAML}")
    echo "Registered agents: ${AGENT_COUNT}"
    echo ""
    yq '.agents[] | "- \(.codename) (\(.id)): \(.role)"' "${AGENTS_YAML}"
else
    echo "yq not found - showing raw YAML"
    grep -A2 "codename:" "${AGENTS_YAML}" || true
fi

echo ""
echo "Agent status: Operational"
EOF
    chmod +x "${scripts_dir}/agent-status.sh"

    # Knowledge sync script
    cat > "${scripts_dir}/knowledge-sync.sh" << 'EOF'
#!/bin/bash
# Knowledge base synchronization utility

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
HERMES_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"

usage() {
    cat << USAGE
Usage: $(basename "$0") [OPTIONS]

Knowledge base synchronization utility.

OPTIONS:
    --cleanup     Remove stale entries
    --external    Sync external sources
    --help        Show this help

USAGE
}

main() {
    local cleanup=false
    local external=false

    while [[ $# -gt 0 ]]; do
        case "$1" in
            --cleanup) cleanup=true; shift ;;
            --external) external=true; shift ;;
            --help) usage; exit 0 ;;
            *) echo "Unknown option: $1"; usage; exit 1 ;;
        esac
    done

    echo "Knowledge sync operations complete"
}

main "$@"
EOF
    chmod +x "${scripts_dir}/knowledge-sync.sh"

    log_success "Utility scripts created"
}

# =============================================================================
# SYSTEMD SERVICES
# =============================================================================

install_systemd_services() {
    log_info "Setting up systemd services..."

    local systemd_src="${SCRIPT_DIR}/systemd"
    if [[ ! -d "$systemd_src" ]]; then
        log_debug "No systemd directory found, skipping service installation"
        return 0
    fi

    # Check if running as root and systemd is available
    if [[ $EUID -ne 0 ]]; then
        log_warning "Not running as root — skipping systemd service installation"
        log_info "To install services manually, run: sudo ${systemd_src}/install.sh"
        return 0
    fi

    if ! command -v systemctl &>/dev/null; then
        log_warning "systemd not available — skipping service installation"
        return 0
    fi

    local agents="alif billy chase korg lathrop satya shel shoshin woz yuval"
    local systemd_dir="/etc/systemd/system"

    for agent in $agents; do
        local svc="$systemd_dir/hermes-$agent.service"
        sed "s/%i/$agent/g" "${systemd_src}/hermes-agent@.service" > "$svc"
        log_debug "Installed $svc"
    done

    cp "${systemd_src}/hermes-team.target" "$systemd_dir/hermes-team.target"
    log_debug "Installed hermes-team.target"

    systemctl daemon-reload
    log_success "Systemd services installed"
    log_info "Run 'systemctl start hermes-team.target' to start all agents"
}

# =============================================================================
# PERMISSIONS
# =============================================================================

set_permissions() {
    log_info "Setting permissions..."

    # Make all scripts executable
    find "${HERMES_ROOT}/scripts" -name "*.sh" -type f -exec chmod +x {} \;

    # Set restrictive permissions on config
    if [[ -d "${CONFIG_DIR}" ]]; then
        chmod 700 "${CONFIG_DIR}"
        chmod 600 "${CONFIG_DIR}"/*.conf "${CONFIG_DIR}"/*.yaml 2>/dev/null || true
    fi

    log_success "Permissions set"
}

# =============================================================================
# HEALTH CHECK
# =============================================================================

run_health_check() {
    log_info "Running health check..."

    local health_status=0

    # Check directories
    for dir in "${CONFIG_DIR}" "${LOG_DIR}" "${TMP_DIR}"; do
        if [[ ! -d "${dir}" ]]; then
            log_error "Directory missing: ${dir}"
            health_status=1
        fi
    done

    # Check config files
    if [[ ! -f "${CONFIG_DIR}/.env" ]]; then
        log_error "Environment file missing"
        health_status=1
    fi

    if [[ ! -f "${CONFIG_DIR}/hermes.conf" ]]; then
        log_error "Hermes config missing"
        health_status=1
    fi

    if [[ "${health_status}" -eq 0 ]]; then
        log_success "Health check passed"
    else
        log_error "Health check failed"
    fi

    return "${health_status}"
}

# =============================================================================
# SUMMARY
# =============================================================================

print_summary() {
    echo ""
    echo "========================================================================"
    echo "Hermes Team Framework Setup Complete"
    echo "========================================================================"
    echo ""
    echo "Version:        ${HERMES_VERSION}"
    echo "Installation:   ${HERMES_ROOT}"
    echo "Environment:    ${HERMES_ENV:-development}"
    echo ""
    echo "Next Steps:"
    echo "  1. Review configuration: ${CONFIG_DIR}/.env"
    echo "  2. Start the framework:  ./scripts/hermes-start.sh"
    echo "  3. Check agent status:   ./scripts/agent-status.sh"
    echo ""
    echo "Documentation:"
    echo "  - README.md      General overview"
    echo "  - AGENTS.md      Agent roster and capabilities"
    echo "  - SKILLS.md      Skill definitions"
    echo "  - SCRIPTS.md     Operational scripts reference"
    echo ""
    echo "========================================================================"
}

# =============================================================================
# MAIN
# =============================================================================

main() {
    local skip_checks=false
    local verbose=false

    # Parse arguments
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --skip-checks) skip_checks=true; shift ;;
            --verbose) verbose=true; DEBUG=1; shift ;;
            --help)
                echo "Usage: $0 [OPTIONS]"
                echo ""
                echo "Options:"
                echo "  --skip-checks   Skip pre-flight validation"
                echo "  --verbose       Enable detailed output"
                echo "  --help          Show this help"
                exit 0
                ;;
            *) echo "Unknown option: $1"; exit 1 ;;
        esac
    done

    echo ""
    echo "Hermes Team Framework Setup"
    echo "Version: ${SETUP_VERSION}"
    echo ""

    if [[ "${skip_checks}" == "false" ]]; then
        check_prerequisites
        check_filesystem
    fi

    create_directories
    setup_environment
    create_config_files
    create_utility_scripts
    install_systemd_services
    set_permissions
    run_health_check || true
    print_summary
}

main "$@"
